const dictionaries: Record<Exclude<Language, "system">, Dict> = {
  en,
  "zh-Hans": zhHans,
  "zh-Hant": zhHant,
};

export function detectLanguage(languages: readonly string[] = navigator.languages): Exclude<Language, "system"> {
  for (const raw of languages) {
    const lang = raw.toLowerCase();
    if (lang.includes("zh-hant") || lang.includes("zh-tw") || lang.includes("zh-hk") || lang.includes("zh-mo")) return "zh-Hant";
    if (lang.includes("zh-hans") || lang.includes("zh-cn") || lang.includes("zh-sg") || lang === "zh" || lang.startsWith("zh-")) return "zh-Hans";
  }
  return "en";
}

export function createTranslator(language: Language) {
  const resolved = language === "system" ? detectLanguage() : language;
  const dict = dictionaries[resolved] || en;
  return {
    language: resolved,
    t: (key: string) => dict[key] || en[key] || key,
  };
}
